/*
Since the machine only increases/decreases numbers by 1 in each step
=> The total steps = sum of all elements in array
 */
#include <algorithm>
#include <cmath>
#include <iostream>
using namespace std;

int main(void){
  int t;  cin >> t;
  while(t--){
    long int n; cin>>n;
    long int i = 0;
    long int *arr = new long int [n];
    for (i = 0; i < n; ++i)     cin >> arr[i];
  }
}